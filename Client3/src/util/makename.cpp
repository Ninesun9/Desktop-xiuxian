QString MakeName::getName()
{
    return kNames.at(QRandomGenerator::global()->bounded(kNames.size()));
}

qreal MakeName::getWuxing(int jieduan)
{
    // 悟性随境界提升
    static const qreal base[] = {1.0, 1.5, 2.2, 3.4, 5.0, 7.5, 11.0, 16.5, 25.0, 37.0};
    if (jieduan >= 0 && jieduan < 10)
        return base[jieduan] * (0.9 + QRandomGenerator::global()->bounded(200) / 1000.0);
    return 1.0;
}

qreal MakeName::getRandData(qreal data, qreal fanwei)
{
    // 在 [data*(1-fanwei/2), data*(1+fanwei/2)] 范围内随机
    const qreal half = data * fanwei / 2.0;
    const qreal offset = QRandomGenerator::global()->generateDouble() * fanwei * data - half;
    return data + offset;
}
